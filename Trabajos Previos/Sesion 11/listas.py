# List slicing in Python
my_list = ['p', 'r', 'a', 'g', 'r', 'a', 'm', 'i', 'z']

# items from index 2 to index 4
print(my_list[2:5])

# items from index 5 to end
print(my_list[5:])

# items beginnig to end
print(my_list[:])